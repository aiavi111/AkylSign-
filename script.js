function render() {
  toggleAccessibilityClass();

  let html = "";

  if (state.screen === "landing") {
    html = renderLanding();
  } else if (state.screen === "register") {
    html = renderRegister();
  } else if (state.screen === "onboarding") {
    html = renderOnboarding();
  } else if (state.screen === "workspace") {
    html = renderWorkspace();
  }

  document.getElementById("app").innerHTML = html + renderToast();
  saveState();
}

function selectRole(roleKey) {
  state.userData = state.userData || { role: "student", form: {} };
  state.userData.role = roleKey;
  render();
}

function submitRegister() {
  const role = state.userData?.role || "student";
  const form = {
    surname: document.getElementById("surname")?.value.trim() || "",
    name: document.getElementById("name")?.value.trim() || "",
    age: document.getElementById("age")?.value.trim() || "",
    email: document.getElementById("email")?.value.trim() || "",
    phone: document.getElementById("phone")?.value.trim() || "",
    company: document.getElementById("company")?.value.trim() || ""
  };

  if (!form.surname || !form.name || !form.age) {
    showToast(t().invalidForm);
    return;
  }

  state.userData = { role, form };
  state.screen = "onboarding";
  render();
}

function toggleGoal(goal) {
  if (state.onboardingGoals.includes(goal)) {
    state.onboardingGoals = state.onboardingGoals.filter((g) => g !== goal);
  } else {
    state.onboardingGoals.push(goal);
  }
  render();
}

function finishOnboarding() {
  state.screen = "workspace";
  state.currentPage = "dashboard";
  render();
}

function goPage(page) {
  state.currentPage = page;
  render();
}

