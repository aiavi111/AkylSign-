function translateText() {
  const input = document.getElementById("translator-input");
  const value = input?.value.trim();
  if (!value) return;

  state.translator.history.unshift({
    id: Date.now(),
    source: "text",
    content: value
  });
  state.translator.history = state.translator.history.slice(0, 8);
  input.value = "";
  render();
}

